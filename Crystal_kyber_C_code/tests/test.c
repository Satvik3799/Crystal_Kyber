uint32_t bit_reverse(uint32_t num, uint32_t logn) {
    uint32_t rev_num = 0;
    for (uint32_t i = 0; i < logn; i++) {
        if ((num >> i) & 1) {
            rev_num |= 1 << (logn - 1 - i);
        }
    }
    return rev_num;
}

//QC - Passed
uint32_t barret_reduction(uint32_t c1) {
    uint32_t p = 0;
    uint32_t m = 5039;
    // uint32_t k = 24;
    uint64_t t123 = (uint64_t)m * c1;
    uint32_t t = t123 >> 24;
    uint64_t ta = (uint64_t)p * t;
    uint32_t c = c1 - ta;
    if (c > p) {
        c = c - p;
    }
    return c;
}

//QC Passed
void butterfly_dit(uint32_t w, uint32_t u, uint32_t v, uint32_t *x, uint32_t *y) {
    uint32_t v1 = barret_reduction((uint64_t)w * v % q);
    *x = (u + v1) % q;
    *y = (u - v1 + q) % q;
}

void butterfly_dif(uint32_t w, uint32_t u, uint32_t v, uint32_t *x, uint32_t *y) {
    *x = (u + v) % q;
    uint32_t y1 = (u - v + q) % q;
    *y = barret_reduction((uint64_t)w * y1 % q);
}

