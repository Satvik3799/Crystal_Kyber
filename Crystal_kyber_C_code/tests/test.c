void ct_ntt(uint32_t *a, uint32_t *psis) {
    uint32_t l = (uint32_t)log2(n);
    uint32_t v = n / 2;
    uint32_t i,s;
    for ( i = 0; i < l; i++) {
        for ( s = 0; s < v; s++) {
            uint32_t ie, io, iw;
            addr_gen(s, i, l, v, &ie, &io, &iw);
            uint32_t S = psis[iw];
            uint32_t U = a[ie];
            uint32_t V = a[io];
            uint32_t x, y;
            butterfly_dit(S, U, V, &x, &y);
            a[ie] = x;
            a[io] = y;
        }
    }
}

void gs_intt(uint32_t *a, uint32_t *inv_psis) {
    uint32_t l = (uint32_t)log2(n);
    uint32_t v = n / 2;
    uint32_t i, s;
    for ( i = 0; i < l; i++) {
        for ( s = 0; s < v; s++) {
            uint32_t i2 = l - i - 1;
            uint32_t ie, io, iw;
            addr_gen(s, i2, l, v, &ie, &io, &iw);
            uint32_t S = inv_psis[iw];
            uint32_t U = a[ie];
            uint32_t V = a[io];
            uint32_t x, y;
            butterfly_dif(S, U, V, &x, &y);
            a[ie] = x;
            a[io] = y;
        }
    }
    for ( i = 0; i < n; i++) {
        a[i] = (uint64_t)a[i] * inv_n % q;
    }
}

